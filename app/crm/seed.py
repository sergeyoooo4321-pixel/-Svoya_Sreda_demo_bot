# ---------------- upsert ----------------

async def _upsert(db, table: str, rows: list[dict[str, Any]], conflict: str) -> int:
    n = 0
    for row in rows:
        cols = list(row.keys())
        placeholders = ", ".join("?" for _ in cols)
        updates = ", ".join(f"{c}=excluded.{c}" for c in cols if c != conflict)
        sql = (
            f"INSERT INTO {table} ({', '.join(cols)}) VALUES ({placeholders}) "
            f"ON CONFLICT({conflict}) DO UPDATE SET {updates}, updated_at=datetime('now')"
        )
        await db.execute(sql, [row[c] for c in cols])
        n += 1
    await db.commit()
    return n


