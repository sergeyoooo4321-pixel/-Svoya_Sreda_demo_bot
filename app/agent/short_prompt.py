def build_context_message(extracted_json: str, summary: str) -> str:
    """Контекстный блок (system) перед последней user-репликой.

    В EXTRACTED_DATA кладём только заполненные поля — пустые лишь зашумляют промт
    (раздел 2: системный промт минимальный; раздел 14: без мусора).
    """
    parts = []
    if summary:
        parts.append(f"SUMMARY ДИАЛОГА:\n{summary}")
    filled = _non_empty_fields(extracted_json)
    if filled:
        parts.append(
            "EXTRACTED_DATA (что уже известно про клиента — не спрашивай повторно):\n"
            + json.dumps(filled, ensure_ascii=False)
        )
    return "\n\n".join(parts)


def _non_empty_fields(extracted_json: str) -> dict:
    if not extracted_json:
        return {}
    try:
        data = json.loads(extracted_json)
    except (ValueError, TypeError):
        return {}
    if not isinstance(data, dict):
        return {}
    return {k: v for k, v in data.items() if v not in (None, "", [], {})}
